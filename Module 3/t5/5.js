// 获取目标元素，这里是 id 为 'pictures' 的元素
const target = document.getElementById('pictures');

// 遍历 picArray 数组，假设它包含多个图片对象
for (let i = 0; i < picArray.length; i++) {
  // 创建一个新的 <article> 元素，并为其添加 'card' 类
  const article = document.createElement('article');
  article.classList.add('card');

  // 创建一个 <h2> 元素，并将其文本设置为当前图片的标题
  const heading = document.createElement('h2');
  heading.textContent = picArray[i].title;

  // 创建一个 <figure> 元素，用于包含图片和图片说明
  const figure = document.createElement('figure');

  // 创建一个 <img> 元素，并设置其源和 alt 文本为图片的属性
  const img = document.createElement('img');
  img.src = picArray[i].image.medium; // 设置图片的 src 属性
  img.alt = picArray[i].title; // 设置图片的 alt 属性

  // 创建一个 <figcaption> 元素，用于显示图片的说明
  const figcaption = document.createElement('figcaption');
  figcaption.textContent = picArray[i].caption;

  // 创建一个 <p> 元素，并将其文本设置为图片的描述
  const paragraph = document.createElement('p');
  paragraph.textContent = picArray[i].description;

  // 将 <img> 和 <figcaption> 元素添加到 <figure> 中
  figure.appendChild(img);
  figure.appendChild(figcaption);

  // 将 <h2>、<figure> 和 <p> 元素添加到 <article> 中
  article.appendChild(heading);
  article.appendChild(figure);
  article.appendChild(paragraph);

  // 将每个 <article> 元素添加到目标容器中
  target.appendChild(article);
}
